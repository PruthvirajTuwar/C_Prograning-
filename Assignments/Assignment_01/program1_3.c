// Program to print 5 to 1 number on screen.

#include<stdio.h>

void Display()
{
    int i = 0;
    
    while ((i <= 5))
    {
        printf("%d\n", i);
        i ++;
    }
    
}
int main()
{
    
    Display();
    
    return 0 ;
}

